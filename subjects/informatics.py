def check_answer(task: dict, user_answer: str) -> tuple[bool, str]:
    if not task.get("answer"):
        return True, "Задание пока без автопроверки."
    user_clean = user_answer.strip().lower().replace(".", ",")
    correct_clean = task["answer"].strip().lower().replace(".", ",")
    aliases = [a.strip().lower().replace(".", ",") for a in task.get("answer_aliases", [])]
    if user_clean == correct_clean or user_clean in aliases:
        return True, task.get("explanation", "")
    return False, task.get("explanation", "")


