def get_definitions(category_key: str) -> str:
    """Вернуть отформатированный текст обозначений для раздела.
    Каждый item — кортеж из 4 или 5 элементов:
    (символ, название, единицы, формула)
    (символ, название, единицы, формула, частые_ошибки)  — необязательно
    """
    if category_key not in DEFINITIONS:
        return "⚠️ Раздел не найден."

    section = DEFINITIONS[category_key]
    lines = [f"📖 *{section['name']}* — обозначения\n"]
    lines.append("_Формат: символ · что это · единицы · где встречается_\n")

    for item in section["items"]:
        if len(item) == 5:
            symbol, name, unit, formula, mistake = item
        else:
            symbol, name, unit, formula = item
            mistake = None

        safe_sym = symbol.replace("_", "\\_").replace("*", "\\*")
        unit_part = f"[{unit}]" if unit else ""
        lines.append(f"• *{safe_sym}* — {name} {unit_part}")
        if formula and formula != "—":
            lines.append(f"  ↳ `{formula}`")
        if mistake:
            # экранируем подчёркивания в тексте, чтобы не порвать Markdown
            safe_mistake = mistake.replace("_", "\\_")
            lines.append(f"  ⚠️ _{safe_mistake}_")

    return "\n".join(lines)


def search_definitions(query: str) -> list[tuple]:
    """Поиск по всем разделам обозначений.
    Возвращает список (название_раздела, item).
    Ищет точное совпадение по символу + подстроку в названии.
    """
    q = query.strip().lower()
    if not q:
        return []
    results = []
    seen_ids = set()
    for cat_key, section in DEFINITIONS.items():
        for item in section["items"]:
            symbol = item[0].lower()
            name = item[1].lower()
            # ID для дедупликации (одна и та же буква в разных разделах — не дубль)
            item_id = (cat_key, item[0])
            if item_id in seen_ids:
                continue
            # критерии:
            # - точное совпадение по символу (с учётом первой буквы перед скобкой)
            symbol_clean = symbol.split("(")[0].split(",")[0].strip()
            match = (
                q == symbol or
                q == symbol_clean or
                (len(q) >= 2 and q in name)
            )
            if match:
                results.append((section["name"], item))
                seen_ids.add(item_id)
    return results
