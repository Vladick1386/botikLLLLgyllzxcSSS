def check_answer(task: dict, user_answer: str) -> tuple[bool, str]:
    """Проверить ответ. Возвращает (правильно?, пояснение)."""
    if not task.get("answer"):
        return True, "Это задание пока без автопроверки."

    correct = task["answer"]
    aliases = task.get("answer_aliases", [])

    # Нормализация: точка как разделитель → запятая, регистр нижний
    user_clean = user_answer.strip().lower().replace(".", ",")
    correct_clean = correct.strip().lower().replace(".", ",")
    alias_clean = [a.strip().lower().replace(".", ",") for a in aliases]

    # Для развёрнутых ответов (18, 19) — ищем ключевые слова
    if len(correct) > 20:
        keywords = alias_clean if alias_clean else [correct_clean]
        found = any(kw in user_clean for kw in keywords)
        if found:
            return True, task.get("explanation", "")
        else:
            return False, task.get("explanation", "")

    # Для кратких ответов — точное совпадение
    if user_clean == correct_clean or user_clean in alias_clean:
        return True, task.get("explanation", "")

    return False, task.get("explanation", "")


