def check_answer(task: dict, user_answer: str) -> tuple[bool, str]:
    if not task.get("answer"):
        return True, "Задание пока без автопроверки."
    user_clean = user_answer.strip().lower().replace(".", ",")
    correct_clean = task["answer"].strip().lower().replace(".", ",")
    aliases = [a.strip().lower().replace(".", ",") for a in task.get("answer_aliases", [])]
    if user_clean == correct_clean or user_clean in aliases:
        return True, task.get("explanation", "")
    return False, task.get("explanation", "")


def get_conspect_topics() -> dict:
    return {k: v["name"] for k, v in CONSPECTS.items()}


def get_conspect(key: str) -> str:
    return CONSPECTS.get(key, {}).get("text", "⚠️ Конспект не найден.")


def get_definition_categories() -> dict:
    return {k: v["name"] for k, v in DEFINITIONS.items()}


def get_definitions(category_key: str) -> str:
    if category_key not in DEFINITIONS:
        return "⚠️ Раздел не найден."
    section = DEFINITIONS[category_key]
    lines = [f"📖 *{section['name']}* — определения\n"]
    lines.append("_Формат: термин · значение · пример_\n")
    for item in section["items"]:
        if len(item) == 5:
            term, name, unit, example, mistake = item
        else:
            term, name, unit, example = item
            mistake = None
        safe_term = term.replace("_", "\\_").replace("*", "\\*")
        lines.append(f"• *{safe_term}* — {name}")
        if example and example != "—":
            lines.append(f"  ↳ _{example}_")
        if mistake:
            safe_mistake = mistake.replace("_", "\\_")
            lines.append(f"  ⚠️ {safe_mistake}")
    return "\n".join(lines)


def search_definitions(query: str) -> list[tuple]:
    q = query.strip().lower()
    if not q:
        return []
    results = []
    seen_ids = set()
    for cat_key, section in DEFINITIONS.items():
        for item in section["items"]:
            term = item[0].lower()
            name = item[1].lower()
            item_id = (cat_key, item[0])
            if item_id in seen_ids:
                continue
            term_clean = term.split("(")[0].split(",")[0].strip()
            match = (
                q == term or
                q == term_clean or
                (len(q) >= 2 and q in name) or
                (len(q) >= 3 and q in term)
            )
            if match:
                results.append((section["name"], item))
                seen_ids.add(item_id)
    return results
